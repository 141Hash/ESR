package OTT;

public class Rota {
    private String fluxo;
    private int custo;
    private OTT origem;
    private OTT destino;
    private String estado;

    public int getCusto() {
        return custo;
    }

    public String getFluxo() {
        return fluxo;
    }

    public OTT getOrigem() {
        return origem;
    }

    public OTT getDestino() {
        return destino;
    }

    public String getEstado() {
        return estado;
    }


}
